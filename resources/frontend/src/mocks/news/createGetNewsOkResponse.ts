import {
  type NewsCollectionResponse,
  type NewsResponse,
} from "../../types/news";

export function createGetNewsOkResponse({
  isLoggedIn,
  numberOfNews,
  page = 1,
  total,
}: {
  isLoggedIn: boolean;
  numberOfNews?: number;
  page?: 1 | 2;
  total?: number;
}): NewsCollectionResponse {
  const news = isLoggedIn ? allNews : publicNews;

  const pageSlice = numberOfNews ?? 10;

  const newsResponse =
    page === 1 ? news.slice(0, pageSlice) : news.slice(pageSlice);

  return {
    data: newsResponse,
    meta: {
      count: numberOfNews ?? news.length,
      total: total ?? news.length,
    },
  };
}

